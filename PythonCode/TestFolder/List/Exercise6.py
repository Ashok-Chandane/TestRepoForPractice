'''
Remove Elements
From the list colors = ["red", "green", "blue", "yellow", "pink"], remove "yellow" and "red" using appropriate list methods.
'''

colors = ["red", "green", "blue", "yellow", "pink"]

print(colors)

colors.remove('yellow')

print(colors)

colors.remove('red')

print(colors)

colors.sort()

print(colors)

colors.reverse()

print(colors)
